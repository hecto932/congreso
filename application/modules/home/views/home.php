                    <div class="col-md-12">
                        <div class="col-md-12">
                            <p style="text-align:justify;">
                                La Facultad de Ciencias Económicas y Sociales (FACES) de la Universidad de Carabobo, a través de la Dirección de Investigación y Producción Intelectual (DIPI) Campus Bárbula y La Morita, con la participación de todas sus estructuras de investigación, extensión y docencia, han programado la realización del Congreso Internacional de Investigación e innovación de la Facultad de Ciencias Económicas y Sociales de la Universidad de Carabobo 2016, “Encuentro de saberes a través del diálogo”, con la finalidad de propiciar un escenario para la promoción,  difusión y  proyección  de las investigaciones y los saberes en el ámbito local, regional, nacional e internacional, concernientes a las ciencias económicas y sociales. Asimismo, proporcionar  un espacio para el debate, la reflexión y el intercambio de conocimientos y experiencias, producto del quehacer investigativo, con pertinencia a los retos y oportunidades de la sociedad actual.
                                <br/>
                                <br/>
                                Bajo el lema “Encuentro de saberes a través del diálogo”, este Congreso Internacional busca fomentar en la comunidad universitaria y en el país en general, el diálogo de saberes como una estrategia para innovar en el campo de las ciencias económicas y sociales y generar respuestas creativas a los complejos escenarios y necesidades que caracterizan la Venezuela actual.

